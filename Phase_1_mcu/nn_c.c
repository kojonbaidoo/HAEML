// Define activation function
float relu(float x) {
    return fmaxf(0, x);
}

// Define softmax function
void softmax(float *x, int n) {
    float max_x = x[0];
    for (int i = 1; i < n; i++) {
        if (x[i] > max_x) {
            max_x = x[i];
        }
    }
    float sum = 0;
    for (int i = 0; i < n; i++) {
        x[i] = expf(x[i] - max_x);
        sum += x[i];
    }
    for (int i = 0; i < n; i++) {
        x[i] /= sum;
    }
}

// Define matrix multiplication function
void matmul(float *A, float *B, float *C, int m, int n, int p) {
    for (int i = 0; i < m; i++) {
        for (int j = 0; j < p; j++) {
            float sum = 0;
            for (int k = 0; k < n; k++) {
                sum += A[i*n+k] * B[k*p+j];
            }
            C[i*p+j] = sum;
        }
    }
}

